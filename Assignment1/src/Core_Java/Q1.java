package Core_Java;

public class Q1 
{
	public static void main(String args[])
	{
		double number1=2.3;
		double number2=4;
	
		System.out.println("number 1 = " + number1);
		System.out.println("number 2 = " + number2);
		
		System.out.println("number 1 + number 2 = " + (number1+number2));
		
		System.out.println("number 1 - number 2 = " + (number1-number2));
		
		System.out.println("number 1 * number 2 = " + (number1*number2));
		
		System.out.println("number 1 / number 2 = " + (number1/number2));
	}
}
